interface I18nState   { locale: Locale }
interface I18nActions { setLocale: (locale: Locale) => void }

export const useLocaleStore = create<I18nState & I18nActions>()(
  persist(
    (set) => ({ locale: 'en', setLocale: (locale) => set({ locale }) }),
    { name: 'dc-locale' }
  )
)

const dictionaries = { en, es } as const

export function useI18n() {
  const { locale, setLocale } = useLocaleStore()
  return { t: dictionaries[locale], locale, setLocale }
}
